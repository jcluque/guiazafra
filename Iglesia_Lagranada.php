        <h1>Iglesia Mayor de Nuestra Señora de la Granada</h1>
        <p>La iglesia Mayor de Nuestra Señora de la Granada se ubica en el municipio de Llerena. Es fruto de un largo proceso constructivo, que se prolongó de los siglos XIV al XVIII, con la consecuente mezcla de estilos: gótico-mudéjar, renacentista, barroco y rococó. En el exterior, destaca la torre, elemento emblemático de la ciudad.
<p>La iglesia Mayor de Nuestra Señora de la Granada se ubica en la plaza Mayor del municipio de Llerena, al sureste de la provincia de Badajoz.

<br>El templo es fruto de un largo proceso constructivo que se inicia a principios del siglo XIV, con la edificación de la primitiva iglesia Mayor de estilo gótico, de la que solo se conservan los dos primeros cuerpos de la torre, la capilla de Zapata o de San Juan Bautista y la capilla del Prior, y finaliza en el siglo XVIII.

<br>En la actualidad, la iglesia, con planta de salón, de espacio uniforme y someramente compartimentado, tiene tres naves separadas por columnas de cantería.

<br>n el interior, destaca la capilla del Licenciado Zapata, situada junto a la cabecera del templo.

<br>De estilo gótico-renacentista, fue fundada en 1498 por Hernando de León, Contador Mayor de Castilla, cuya condena por herejía propició la subasta de la capilla, que fue adquirida por el licenciado Luis Zapata.

<br>Su planta es poligonal, dotada de sacristía propia y campanario.

<br>Por su parte, la capilla del Prior, fundada por el prior Gonzalo de la Fuente en 1580, es una construcción ya plenamente renacentista.

<br>En su interior destaca la portada de acceso a la sacristía, coronada por un frontón con el blasón del fundador.

<br>Adosado a la cabecera del templo se encuentra el camarín de Nuestra Señora de la Granada, consagrado a la imagen de la Virgen, con un estilo rococó propio de finales del siglo XVIII, de planta octogonal y profusamente decorada.

<br>En el exterior, destaca la torre de estilo gótico-mudéjar y renacentista, que se ha convertido en emblema de la ciudad.

<br>Cuenta con cinco cuerpos, los dos primeros de estilo gótico-mudéjar y el resto, de estilo renacentista, construidos en ladrillo, en la segunda mitad del siglo XVI.

<br>En la cima de la torre, sobre una terraza abalaustrada, se levantan cuatro torrecillas de planta circular, cubiertas por cupulines de cerámica vidriada.

<br>La iglesia Mayor de Nuestra Señora de la Granada es una visita indispensable para el amante del patrimonio histórico, con su mezcla de estilos arquitectónicos y el atractivo entorno en el que se ubica, en la bella plaza Mayor porticada de Llerena.
